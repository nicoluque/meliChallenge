package com.meli.persistence.entity.country;

public class Translations {
	private String br;
	private String de;
	private String es;
	private String fa;
	private String fr;
	private String hr;
	private String it;
	private String ja;
	private String nl;
	private String pt;
	public Translations(String br, String de, String es, String fa, String fr, String hr, String it, String ja,
			String nl, String pt) {
		super();
		this.br = br;
		this.de = de;
		this.es = es;
		this.fa = fa;
		this.fr = fr;
		this.hr = hr;
		this.it = it;
		this.ja = ja;
		this.nl = nl;
		this.pt = pt;
	}
	public String getBr() {
		return br;
	}
	public void setBr(String br) {
		this.br = br;
	}
	public String getDe() {
		return de;
	}
	public void setDe(String de) {
		this.de = de;
	}
	public String getEs() {
		return es;
	}
	public void setEs(String es) {
		this.es = es;
	}
	public String getFa() {
		return fa;
	}
	public void setFa(String fa) {
		this.fa = fa;
	}
	public String getFr() {
		return fr;
	}
	public void setFr(String fr) {
		this.fr = fr;
	}
	public String getHr() {
		return hr;
	}
	public void setHr(String hr) {
		this.hr = hr;
	}
	public String getIt() {
		return it;
	}
	public void setIt(String it) {
		this.it = it;
	}
	public String getJa() {
		return ja;
	}
	public void setJa(String ja) {
		this.ja = ja;
	}
	public String getNl() {
		return nl;
	}
	public void setNl(String nl) {
		this.nl = nl;
	}
	public String getPt() {
		return pt;
	}
	public void setPt(String pt) {
		this.pt = pt;
	}
	
	
}
